export function getRecipeText(recipe: Recipe, language: Language) {
  if (language === "pt") return recipe;
  const { title, line } = getRecipeReplacements(language);
  const localizedTitle = applyReplacements(recipe.title, title);
  const ingredients = recipe.ingredients.map((l) => applyReplacements(l, line));
  const preparation = recipe.preparation.map((l) => applyReplacements(l, line));
  return { ...recipe, title: localizedTitle, ingredients, preparation };
}
